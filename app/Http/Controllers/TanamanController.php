    {
        $validate = $request->validate([
            'nama_tanaman' => 'required|max:255|unique:tanamans',
            'famili' => 'required',
            'jenis' => 'required',
            'habitat_id' => 'required',
            'deskripsi' => 'required',

        ]);



        Tanaman::create($validate);

        return redirect('/tanaman');
    }

